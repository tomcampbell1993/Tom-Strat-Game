using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class IntCoord2
{
    int x;
    int y;

    public IntCoord2(int xCoord, int yCoord)
    {
        x = xCoord;
        y = yCoord;
    }

    public int Getx()
    {
        return x;
    }

    public int Gety()
    {
        return y;
    }

}
